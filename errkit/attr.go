package errkit

// AttrResource is a resource attribute
// which can be used to represent a resource
type AttrResource struct {
	// Type is the type of resource
	Type string
	// Value is the value of resource
	Value string
}

// AttrAction is an action attribute
// which can be used to represent an action
type AttrAction struct {
	// Type is the type of action
	Type string
	// Value is the value of action
	Value string
}
